--1.PAGEVIEW
SELECT MAX(C.ID) MAXID FROM ODSHAPPIGO.ODS_PAGEVIEW C;

SELECT A.CREATEON
  FROM ODSHAPPIGO.ODS_PAGEVIEW A
 WHERE EXISTS (SELECT 1
          FROM (SELECT MAX(C.ID) MAXID FROM ODSHAPPIGO.ODS_PAGEVIEW C) B
         WHERE A.ID = B.MAXID);

--2.SEARCH
SELECT MAX(A.ID) FROM ODSHAPPIGO.ODS_SEARCH A;

SELECT MAX(A.CREATEON) FROM ODSHAPPIGO.ODS_SEARCH A;


--3.SHOPPINGCAR
SELECT MAX(A.ID) FROM ODSHAPPIGO.ODS_SHOPPINGCAR A;

SELECT MAX(A.CREATEON) FROM ODSHAPPIGO.ODS_SHOPPINGCAR A;
