SELECT
  A.SALES_SOURCE_SECOND_NAME,
  SUM(A.EFFECTIVE_ORDER_AMOUNT)
FROM OPER_PRODUCT_DAILY_REPORT A
WHERE A.POSTING_DATE_KEY = 20180605
      AND A.SALES_SOURCE_NAME = '电商'
GROUP BY A.SALES_SOURCE_SECOND_NAME;

