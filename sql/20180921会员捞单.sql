/*
6-8月在新媒体订购超过3次的用户
*/
--订购
SELECT F.MEMBER_BP
  FROM (SELECT TO_NUMBER(A.CUST_NO) MEMBER_BP, COUNT(A.ORDER_ID) ORDER_CNT
          FROM FACT_EC_ORDER_2 A
         WHERE A.ADD_TIME BETWEEN DATE '2017-11-01' AND DATE
         '2018-09-30'
           AND A.ORDER_STATE >= 20
           AND A.CUST_NO <> 0
         GROUP BY A.CUST_NO
        HAVING COUNT(A.ORDER_ID) >= 3) F
 ORDER BY F.MEMBER_BP;
