/*
捞取10月订购过的用户，按次数分
*/
--订购
SELECT TO_NUMBER(A.CUST_NO) MEMBER_BP, COUNT(A.ORDER_ID) ORDER_CNT
  FROM FACT_EC_ORDER_2 A
 WHERE A.ADD_TIME BETWEEN DATE '2018-10-01' AND DATE '2018-10-31'
   AND A.ORDER_STATE >= 20
   AND A.CUST_NO <> 0
 GROUP BY TO_NUMBER(A.CUST_NO)
 ORDER BY COUNT(A.ORDER_ID) DESC;
