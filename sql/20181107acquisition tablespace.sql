--3.DATA_ACQUISITION_ITEM
SELECT *
  FROM (SELECT A.TABLE_OWNER OWNER,
               A.TABLE_NAME OBJECT_NAME,
               A.SUBPARTITION_NAME,
               1 COL1,
               'ALTER TABLE ' || A.TABLE_OWNER || '.' || A.TABLE_NAME ||
               ' MOVE SUBPARTITION ' || A.SUBPARTITION_NAME ||
               ' TABLESPACE ACQUISITION_DATA PARALLEL(DEGREE 4);' COL2
          FROM DBA_TAB_SUBPARTITIONS A
         WHERE A.TABLE_NAME = 'DATA_ACQUISITION_ITEM'
        UNION ALL
        SELECT A.INDEX_OWNER OWNER,
               A.INDEX_NAME OBJECT_NAME,
               A.SUBPARTITION_NAME,
               2 COL1,
               'ALTER INDEX ' || A.INDEX_OWNER || '.' || A.INDEX_NAME ||
               ' REBUILD SUBPARTITION ' || A.SUBPARTITION_NAME ||
               ' TABLESPACE ACQUISITION_INDEX PARALLEL(DEGREE 4);' COL2
          FROM DBA_IND_SUBPARTITIONS A
         WHERE A.INDEX_NAME IN
               (SELECT A.INDEX_NAME
                  FROM DBA_INDEXES A
                 WHERE A.table_name = 'DATA_ACQUISITION_ITEM')
        UNION ALL
        SELECT A.INDEX_OWNER OWNER,
               A.INDEX_NAME OBJECT_NAME,
               A.SUBPARTITION_NAME,
               3 COL1,
               'ALTER INDEX ' || A.INDEX_OWNER || '.' || A.INDEX_NAME ||
               ' REBUILD SUBPARTITION ' || A.SUBPARTITION_NAME ||
               ' NOPARALLEL;' COL2
          FROM DBA_IND_SUBPARTITIONS A
         WHERE A.INDEX_NAME IN
               (SELECT A.INDEX_NAME
                  FROM DBA_INDEXES A
                 WHERE A.table_name = 'DATA_ACQUISITION_ITEM')) B
 ORDER BY B.OWNER, B.SUBPARTITION_NAME, B.OBJECT_NAME, B.COL1;

