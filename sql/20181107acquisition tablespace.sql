               1 COL1,
               'ALTER TABLE ' || A.TABLE_OWNER || '.' || A.TABLE_NAME ||
               ' MOVE PARTITION ' || A.PARTITION_NAME ||
               ' TABLESPACE ACQUISITION_DATA PARALLEL(DEGREE 4) NOLOGGING;' COL2
          FROM DBA_TAB_PARTITIONS A
         WHERE A.TABLE_NAME = 'DATA_ACQUISITION_ITEM_CURRENT'
        UNION ALL
        SELECT A.INDEX_OWNER OWNER,
               A.INDEX_NAME OBJECT_NAME,
               A.PARTITION_NAME,
               2 COL1,
               'ALTER INDEX ' || A.INDEX_OWNER || '.' || A.INDEX_NAME ||
               ' REBUILD PARTITION ' || A.PARTITION_NAME ||
               ' TABLESPACE ACQUISITION_INDEX PARALLEL(DEGREE 4) NOLOGGING;' COL2
          FROM DBA_IND_PARTITIONS A
         WHERE A.SUBPARTITION_COUNT = 0
           AND A.INDEX_NAME IN
               (SELECT A.INDEX_NAME
                  FROM DBA_INDEXES A
                 WHERE A.table_name = 'DATA_ACQUISITION_ITEM_CURRENT')
        UNION ALL
        SELECT A.INDEX_OWNER OWNER,
               A.INDEX_NAME OBJECT_NAME,
               A.PARTITION_NAME,
               3 COL1,
               'ALTER INDEX ' || A.INDEX_OWNER || '.' || A.INDEX_NAME ||
               ' REBUILD PARTITION ' || A.PARTITION_NAME || ' NOPARALLEL;' COL2
          FROM DBA_IND_PARTITIONS A
         WHERE A.SUBPARTITION_COUNT = 0
           AND A.INDEX_NAME IN
               (SELECT A.INDEX_NAME
                  FROM DBA_INDEXES A
                 WHERE A.table_name = 'DATA_ACQUISITION_ITEM_CURRENT')) B
