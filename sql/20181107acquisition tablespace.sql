SELECT A.OWNER,
       A.SEGMENT_NAME,
       A.PARTITION_NAME,
       A.SEGMENT_TYPE,
       A.SEGMENT_SUBTYPE,
       A.TABLESPACE_NAME,
       A.HEADER_FILE,
       A.HEADER_BLOCK,
       A.BYTES / 1024 / 1024 / 1024 SIZE_GB,
       A.BLOCKS,
       A.EXTENTS,
       A.INITIAL_EXTENT,
       A.NEXT_EXTENT,
       A.MIN_EXTENTS,
       A.MAX_EXTENTS,
       A.MAX_SIZE,
       A.RETENTION,
       A.MINRETENTION,
       A.PCT_INCREASE,
       A.FREELISTS,
       A.FREELIST_GROUPS,
       A.RELATIVE_FNO,
       A.BUFFER_POOL,
       A.FLASH_CACHE,
       A.CELL_FLASH_CACHE
  FROM DBA_SEGMENTS A
 WHERE A.SEGMENT_NAME = 'DATA_ACQUISITION_ITEM_BASE'
      --AND A.SEGMENT_TYPE LIKE 'INDEX%'
   AND A.TABLESPACE_NAME NOT IN ('ACQUISITION_DATA', 'ACQUISITION_INDEX')
 ORDER BY A.BYTES DESC;

