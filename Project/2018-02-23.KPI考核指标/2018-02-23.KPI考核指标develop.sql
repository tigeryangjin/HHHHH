     T.VISIT_COUNT,
     T.ORDER_COUNT,
     T.W_INSERT_DT,
     T.W_UPDATE_DT)
  VALUES
    (S.DATE_KEY,
     S.VISIT_COUNT,
     S.ORDER_COUNT,
     S.W_INSERT_DT,
     S.W_UPDATE_DT);

