           GROUP BY A1.ADD_TIME) A,
         (SELECT B1.MEMBER_TIME DATE_KEY,
                 COUNT(DISTINCT B1.MEMBER_CRMBP) REG_MEMBER_COUNT
            FROM FACT_ECMEMBER B1
           WHERE B1.MEMBER_TIME BETWEEN 20171101 AND 20171131
           GROUP BY B1.MEMBER_TIME) B
