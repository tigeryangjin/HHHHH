--6,7月份1.0渠道取消的订单
SELECT *
  FROM FACT_ORDER A
 WHERE A.POSTING_DATE_KEY BETWEEN 20170601 AND 20170718
   AND A.SALES_SOURCE_SECOND_KEY IN (20001, 20006, 20015)
	 /*AND A.ORDER_STATE=0*/;
	 
	 
SELECT *
  FROM FACT_ORDER_REVERSE A
 WHERE A.POSTING_DATE_KEY BETWEEN 20170601 AND 20170718
   AND A.SALES_SOURCE_SECOND_KEY IN (20001, 20006, 20015);
	 
SELECT *
  FROM FACT_ORDER A
 WHERE A.POSTING_DATE_KEY BETWEEN 20170601 AND 20170718
   AND A.SALES_SOURCE_SECOND_DESC IN ('A20001', 'A20006', 'A20015');
